<h2>Help</h2>

<section>
  <h3>Profiles</h3>
  <p>
    A profile defines the rules associated with a collection of pockets.
  </p>

  <p>
    Each profile is associated with a preset definition that defines core currency
    definitions, encumbrance rules, and a list of pockets that work with
    these rules. You can add additional currency valuations and pocket definitions
    as you wish. You can also import items (from one of a few sources or a preset)
    to make it easier to move items into and out of your pockets.
  </p>

  <p>
    Each profile also defines how (or if) changes to the contents of your pockets
    should be externalized, synced or shared.
  </p>

  <h3>Import and Export</h3>

  <p>Core profile settings (currency, encumbrance, pockets, items) can be imported or exported
    to make it easier to share a configuration with others.</p>

</section>

